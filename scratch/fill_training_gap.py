# Generate and add new queries
new_records = []
for intent, templates in INTENT_TEMPLATES.items():
    current = current_counts.get(intent, 0)
    needed = max(0, target_per_intent - current)
    
    if needed == 0:
        print(f"  {intent}: already at {current}, no gap")
        continue
    
    available = [t for t in templates if t.strip().lower() not in existing_questions]
    take = min(needed, len(available))
    
    selected = random.sample(available, take) if take < len(available) else available[:take]
    for q in selected:
        new_records.append({"question": q, "intent label": intent})
        existing_questions.add(q.strip().lower())
    
    print(f"  {intent}: current={current}, needed={needed}, adding={take}")

print(f"\nGenerated {len(new_records)} additional queries")

# Append to v2
new_df = pd.DataFrame(new_records)
v2_combined = pd.concat([v2_df, new_df], ignore_index=True)

# Shuffle
v2_combined = v2_combined.sample(frac=1, random_state=42).reset_index(drop=True)

# Remove duplicates
before = len(v2_combined)
v2_combined = v2_combined.drop_duplicates(subset=["question"], keep="first")
print(f"After dedup: {len(v2_combined)} (removed {before - len(v2_combined)})")

